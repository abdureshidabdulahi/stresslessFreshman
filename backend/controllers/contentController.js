const getContent = (req, res) => {
  const { category, featured } = req.query;
  let content = [...CONTENT_LIBRARY];

  if (category) {
    content = content.filter((c) => c.category === category);
  }

  if (featured === 'true') {
    content = content.filter((c) => c.featured);
  }

  // Return without full content for listing
  const listing = content.map(({ content: _, ...rest }) => rest);

  res.json({ success: true, count: listing.length, articles: listing, categories: CATEGORIES });
};

// @desc    Get single content article
// @route   GET /api/content/:id
// @access  Public
const getArticle = (req, res) => {
  const article = CONTENT_LIBRARY.find((a) => a.id === req.params.id);
  if (!article) {
    return res.status(404).json({ message: 'Article not found' });
  }
  res.json({ success: true, article, categories: CATEGORIES });
};

